    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $user = Auth::user();
        if($user->company_code == 0){
            $company = Company::paginate(10);
        } else {
            $company = Company::where('code',$user->company_code)->paginate(10);
        }
        if ($request->ajax()){
            $company = Company::search($request->all())->paginate(SettingConstant::DEFAULT_PAGINATE);
            return  view('settings.company.ajax',['company'=>$company]);
        }
        return  view('settings.company.index',['company'=>$company]);
    }

